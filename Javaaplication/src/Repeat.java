public class Repeat {
    
    boolean life = true; 

    public void start() {
        while (life) {
            eat();
            sleep();
            code();
            
            if (life == false) {
                break;
            }
        }
    }

    public void eat() {
        System.out.println("Eating...");
    }

    public void sleep() {
        System.out.println("Sleeping...");
    }

    public void code() {
        System.out.println("Coding...");
    }

    public static void main(String[] args) {
        Repeat repeat = new Repeat();
        repeat.start();
    }
}
